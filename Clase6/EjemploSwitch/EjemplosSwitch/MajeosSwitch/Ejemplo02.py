numeroDia= int(input("Ingrese el numero del dia de la semana "))
if numeroDia == 1:
    print("Dia %d es Lunes"%(numeroDia))
elif numeroDia == 2:
    print("Dia %d es Martes"%(numeroDia))
elif numeroDia == 3:
    print("Dia %d es Miercoles"%(numeroDia))
elif numeroDia == 4:
    print("Dia %d es Jueves"%(numeroDia))
elif numeroDia == 5:
    print("Dia %d es Viernes"%(numeroDia))
elif numeroDia == 6:
    print("Dia %d es Sabado"%(numeroDia))
elif numeroDia == 7:
    print("Dia %d es Domingo"%(numeroDia))
else:
    print("Opcion incorrecta")